package org.ghast.grest.presentation.model;

public class CacheWeek {
	
	private String id_week;
	private String free_num;
	
	
	public String getId_week() {
		return id_week;
	}
	public void setId_week(String id_week) {
		this.id_week = id_week;
	}
	public String getFree_num() {
		return free_num;
	}
	public void setFree_num(String free_num) {
		this.free_num = free_num;
	}
	

}
