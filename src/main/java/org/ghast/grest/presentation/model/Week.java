package org.ghast.grest.presentation.model;

public class Week {
	
	private String id;
	private String num;
	private String trip_price;
	private String trip_num;
	private String free_num;
	private String grest_oratory_id;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getTrip_price() {
		return trip_price;
	}
	public void setTrip_price(String trip_price) {
		this.trip_price = trip_price;
	}
	public String getTrip_num() {
		return trip_num;
	}
	public void setTrip_num(String trip_num) {
		this.trip_num = trip_num;
	}
	public String getFree_num() {
		return free_num;
	}
	public void setFree_num(String free_num) {
		this.free_num = free_num;
	}
	public String getGrest_oratory_id() {
		return grest_oratory_id;
	}
	public void setGrest_oratory_id(String grest_oratory_id) {
		this.grest_oratory_id = grest_oratory_id;
	}

}
