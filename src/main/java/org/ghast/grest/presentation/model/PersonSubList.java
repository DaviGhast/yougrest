package org.ghast.grest.presentation.model;

public class PersonSubList {
	
	private String id;
	private String surname;
	private String name;
	private String school_class;
	private String oratory_location;
	private String person_id;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSchool_class() {
		return school_class;
	}
	public void setSchool_class(String school_class) {
		this.school_class = school_class;
	}
	public String getOratory_location() {
		return oratory_location;
	}
	public void setOratory_location(String oratory_location) {
		this.oratory_location = oratory_location;
	}
	public String getPerson_id() {
		return person_id;
	}
	public void setPerson_id(String person_id) {
		this.person_id = person_id;
	}
	
	
	
		

}
