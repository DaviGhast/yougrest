package org.ghast.grest.presentation.model;

public class Grest {
	
	private String id;
	private String name;
	private String year;
	private String num_weeks;
	private String start_date;
	private String sub_price;
	private String week_price;
	private String lunch_price;
	private String pool_price;
	private String pool_day;
	private String trip_day;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getNum_weeks() {
		return num_weeks;
	}
	public void setNum_weeks(String num_weeks) {
		this.num_weeks = num_weeks;
	}
	public String getStart_date() {
		return start_date;
	}
	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}
	public String getSub_price() {
		return sub_price;
	}
	public void setSub_price(String sub_price) {
		this.sub_price = sub_price;
	}
	public String getWeek_price() {
		return week_price;
	}
	public void setWeek_price(String week_price) {
		this.week_price = week_price;
	}
	public String getLunch_price() {
		return lunch_price;
	}
	public void setLunch_price(String lunch_price) {
		this.lunch_price = lunch_price;
	}
	public String getPool_price() {
		return pool_price;
	}
	public void setPool_price(String pool_price) {
		this.pool_price = pool_price;
	}
	public String getPool_day() {
		return pool_day;
	}
	public void setPool_day(String pool_day) {
		this.pool_day = pool_day;
	}
	public String getTrip_day() {
		return trip_day;
	}
	public void setTrip_day(String trip_day) {
		this.trip_day = trip_day;
	}
	
		

}
