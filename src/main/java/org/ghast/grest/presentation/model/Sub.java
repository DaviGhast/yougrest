package org.ghast.grest.presentation.model;

public class Sub {
	
	private String id;
	private String squad;
	private String tshirt;
	private String note;
	private String grest_id;
	private String oratory_id;
	private String person_id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSquad() {
		return squad;
	}
	public void setSquad(String squad) {
		this.squad = squad;
	}
	public String getTshirt() {
		return tshirt;
	}
	public void setTshirt(String tshirt) {
		this.tshirt = tshirt;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getGrest_id() {
		return grest_id;
	}
	public void setGrest_id(String grest_id) {
		this.grest_id = grest_id;
	}
	public String getOratory_id() {
		return oratory_id;
	}
	public void setOratory_id(String oratory_id) {
		this.oratory_id = oratory_id;
	}
	public String getPerson_id() {
		return person_id;
	}
	public void setPerson_id(String person_id) {
		this.person_id = person_id;
	}
	
	
	
	

}
